async function processLineByLine(input, right, down) {
  var arr = input.split('\n');
  var total = 0;

  var length = arr[0].length;

  for (
    let rowIndex = 0 + down, colIndex = 0;
    rowIndex < arr.length;
    rowIndex += down
  ) {
    if (colIndex + right - length >= 0) {
      colIndex = colIndex + right - length;
    } else {
      colIndex += right;
    }
    if (arr[rowIndex][colIndex] === '#') {
      total++;
    }
  }

  console.log({ total });
}

